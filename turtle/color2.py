from turtle import Turtle
c2=Turtle()
c2.getscreen()
c2.shape("turtle")
c2.color("red","green")
c2.begin_fill()
c2.circle(100)
c2.end_fill()
c2.rt(130)
c2.penup()
c2.forward(100)
c2.pendown()
c2.pensize(10)
c2.color("red","orange")
c2.begin_fill()
c2.goto(-100,-100)
c2.circle(80)
c2.end_fill()

c2.screen.exitonclick()